#!/usr/bin/env node

const { appendFileSync, existsSync, readdirSync, readFileSync } = require("node:fs");
const { join } = require("node:path");
const { spawnSync } = require("node:child_process");

const root = process.cwd();
const changesDir = join(root, "openspec", "changes");
const dryRun = process.argv.includes("--dry-run");
const archiveIndex = process.argv.indexOf("--archive");
const archiveChange = archiveIndex === -1 ? null : process.argv[archiveIndex + 1];

function run(command, args) {
  const result = spawnSync(command, args, {
    cwd: root,
    stdio: "inherit",
  });

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

function setOutput(name, value) {
  if (!process.env.GITHUB_OUTPUT) {
    return;
  }

  appendFileSync(process.env.GITHUB_OUTPUT, `${name}<<EOF\n${value}\nEOF\n`);
}

function taskState(tasksPath) {
  const lines = readFileSync(tasksPath, "utf8").split(/\r?\n/);
  const boxes = lines
    .map((line) => line.match(/^\s*-\s+\[([ xX])\]/))
    .filter(Boolean)
    .map((match) => match[1]);

  return {
    hasTasks: boxes.length > 0,
    completedCount: boxes.filter((box) => box.toLowerCase() === "x").length,
    taskCount: boxes.length,
    complete: boxes.length > 0 && boxes.every((box) => box.toLowerCase() === "x"),
  };
}

function prMetadata(change) {
  const workflowUrl = `https://github.com/${process.env.GITHUB_REPOSITORY}/blob/main/.github/workflows/archive-completed-openspec-changes.yml`;

  return {
    title: `docs(opencode): sync \`${change.name}\``,
    body: `Archives OpenSpec change \`${change.name}\` with ${change.completedCount}/${change.taskCount} completed tasks.\n\nGenerated by [archive completed OpenSpec changes workflow](${workflowUrl}).`,
  };
}

function completedChange(change) {
  const tasksPath = join(changesDir, change, "tasks.md");

  if (!existsSync(tasksPath)) {
    return null;
  }

  const state = taskState(tasksPath);
  return state.hasTasks && state.complete
    ? {
        name: change,
        completedCount: state.completedCount,
        taskCount: state.taskCount,
      }
    : null;
}

if (!existsSync(changesDir)) {
  console.log("No openspec/changes directory found.");
  process.exit(0);
}

const completedChanges = readdirSync(changesDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory() && entry.name !== "archive")
  .map((entry) => entry.name)
  .map(completedChange)
  .filter(Boolean);

if (completedChanges.length === 0) {
  setOutput("has_changes", "false");
  setOutput("changes", "[]");
  console.log("No completed OpenSpec changes found.");
  process.exit(0);
}

setOutput("has_changes", "true");
setOutput("changes", JSON.stringify(completedChanges));

console.log(`Completed OpenSpec changes: ${completedChanges.map((change) => change.name).join(", ")}`);

if (dryRun) {
  process.exit(0);
}

if (!archiveChange) {
  process.exit(0);
}

const change = completedChanges.find((candidate) => candidate.name === archiveChange);

if (!change) {
  console.error(`OpenSpec change is not complete: ${archiveChange}`);
  process.exit(1);
}

const metadata = prMetadata(change);
setOutput("pr_title", metadata.title);
setOutput("pr_body", metadata.body);

run("openspec", ["archive", change.name, "--yes"]);

run("openspec", ["validate", "--all", "--strict", "--no-interactive"]);
